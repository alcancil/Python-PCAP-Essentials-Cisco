# Existem dois operadores de comparação
# >= maior ou igual a 
# <= menor ou igaul a 
# Exemplos
var = 1
print("1 - maior ou igal a 1 ?", 1 >= var)
print("2 - maior ou igal a 1 ?", 2 >= var)
print("0 - maior ou igal a 1 ?", 0 >= var)

