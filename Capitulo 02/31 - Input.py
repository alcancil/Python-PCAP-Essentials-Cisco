# Função Input()
#
# A Função Input() serve como um complemento a função print(). Enquanto a função print() imprimi na tela os argumentos fornecidos, a função input()
# fica esperando o usuário entrar com os dados através do teclado, por exemplo
#
print("Tell me anything...")
anything = input()
