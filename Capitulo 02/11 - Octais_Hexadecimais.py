# A função printe aceita literais dos tipos: inteiro, octais e hexadecimais
# Para se utilizar números inteiros basta colocar eles dentro das chaves ( ) . OBS: nunca usar o 0 no ínicio.
# Para se utilizar números octais, utilizar (0o sequência de digitos de 0 a 7)
# Para se utilizar números hexadecimais, utilizar ( 0x sequencia de digitos entre 0 a 9 e letras de A a F)
# OBS: o python já converte automaticamente o resultado para decimal e imprime na tela
#
# imprimir inteiros
print(123)
#
# imprimir octais
print(0o123)
print(0o01234567)
#
# imprimir hexadecimais
print(0x0123456789ABCDEF)
print(0x123)

