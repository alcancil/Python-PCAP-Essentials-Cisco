# Variaveis são utilizadas para armazenar algum conteudo. Ela pode ser um simples número, uma string ou mesmo o resultado de uma conta.
# Obs: para a criação dos nomes das variavies vale diser que elas são case sensitive. Existem palavras reservadas como import que não pode ser usadas
#
